void ADC_to_Modbus(USHORT* msb,USHORT* lsb, uint16_t adc_in_value)
{
	msb[0] = (uint8_t)((adc_in_value&0xff00)>>8);
	lsb[0] = (uint8_t)adc_in_value&0x00ff;
}

void GetTick_to_Modbus(USHORT *a, USHORT *b, USHORT *c, USHORT *d)
{
	a[0] =(uint8_t)((HAL_GetTick()&0xff000000)>>24);
	b[0] =(uint8_t)((HAL_GetTick()&0x00ff0000)>>16);
	c[0] =(uint8_t)((HAL_GetTick()&0x0000ff00)>>8);
	d[0] =(uint8_t)(HAL_GetTick()&0x000000ff);
}

